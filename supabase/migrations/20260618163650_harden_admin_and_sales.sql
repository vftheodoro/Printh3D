create or replace function public.admin_check_login_rate_limit(
  p_identifier_hash text,
  p_max_attempts integer default 5,
  p_window_minutes integer default 15
)
returns table(allowed boolean, retry_after_seconds integer)
language sql
security invoker
set search_path = ''
as $$
  select
    case
      when attempt.identifier_hash is null then true
      when attempt.window_started_at
        < now() - make_interval(mins => greatest(p_window_minutes, 1))
        then true
      else attempt.attempt_count < greatest(p_max_attempts, 1)
    end as allowed,
    case
      when attempt.identifier_hash is null then 0
      when attempt.window_started_at
        < now() - make_interval(mins => greatest(p_window_minutes, 1))
        then 0
      else greatest(
        0,
        extract(
          epoch from (
            attempt.window_started_at
            + make_interval(mins => greatest(p_window_minutes, 1))
            - now()
          )
        )::integer
      )
    end as retry_after_seconds
  from (select 1) seed
  left join public.admin_login_attempts attempt
    on attempt.identifier_hash = p_identifier_hash;
$$;

create or replace function public.admin_record_login_attempt(
  p_identifier_hash text,
  p_success boolean,
  p_window_minutes integer default 15
)
returns void
language plpgsql
security invoker
set search_path = ''
as $$
begin
  if p_success then
    delete from public.admin_login_attempts
    where identifier_hash = p_identifier_hash;
    return;
  end if;

  insert into public.admin_login_attempts (
    identifier_hash,
    attempt_count,
    window_started_at,
    updated_at
  )
  values (p_identifier_hash, 1, now(), now())
  on conflict (identifier_hash) do update
  set
    attempt_count = case
      when public.admin_login_attempts.window_started_at
        < now() - make_interval(mins => greatest(p_window_minutes, 1))
        then 1
      else public.admin_login_attempts.attempt_count + 1
    end,
    window_started_at = case
      when public.admin_login_attempts.window_started_at
        < now() - make_interval(mins => greatest(p_window_minutes, 1))
        then now()
      else public.admin_login_attempts.window_started_at
    end,
    updated_at = now();
end;
$$;


grant execute on function public.admin_delete_sale_to_trash(integer)
  to service_role;

create or replace function public.admin_restore_trash(p_trash_id integer)
returns void
language plpgsql
security invoker
set search_path = ''
as $$
declare
  item public.trash%rowtype;
begin
  select * into item
  from public.trash
  where id = p_trash_id
  for update;

  if not found then
    raise exception 'Item não encontrado na lixeira.';
  end if;

  case item.source_store
    when 'products' then
      insert into public.products
      select (jsonb_populate_record(null::public.products, item.payload)).*;
    when 'sales' then
      insert into public.sales
      select (jsonb_populate_record(null::public.sales, item.payload)).*;
    when 'expenses' then
      insert into public.expenses
      select (jsonb_populate_record(null::public.expenses, item.payload)).*;
    else
      raise exception 'Origem da lixeira não permitida.';
  end case;

  delete from public.trash where id = p_trash_id;
end;
$$;

create or replace function public.admin_purge_expired_trash()
returns integer
language plpgsql
security invoker
set search_path = ''
as $$
declare
  deleted_count integer;
begin
  delete from public.trash where expires_at < now();
  get diagnostics deleted_count = row_count;
  return deleted_count;
end;
$$;


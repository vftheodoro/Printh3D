declare
  target_table text;
  sequence_name text;
begin
  foreach target_table in array array[
    'admin_users',
    'categories',
    'products',
    'product_files',
    'sales',
    'sale_files',
    'clients',
    'expenses',
    'promotions',
    'coupons',
    'settings',
    'trash'
  ]
  loop
    if exists (
      select 1
      from information_schema.columns
      where table_schema = 'public'
        and information_schema.columns.table_name = target_table
        and column_name = 'id'
        and is_identity = 'NO'
        and column_default is null
    ) then
      execute format(
        'alter table public.%I alter column id add generated by default as identity',
        target_table
      );
    end if;

    sequence_name := pg_get_serial_sequence(format('public.%I', target_table), 'id');
    if sequence_name is not null then
      execute format(
        'select setval(%L, greatest(coalesce((select max(id) from public.%I), 0), 1), true)',
        sequence_name,
        target_table
      );
    end if;
  end loop;
end
$$;

